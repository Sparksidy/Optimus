	std::vector<char> ReadFile(const std::string& filename)
	{
		std::ifstream input(filename, std::ios::ate | std::ios::binary);
		if (!input.is_open())
		{
			OP_CORE_ERROR("Unable to open up the file: {0} at line: {1}", filename, __LINE__);
			throw std::runtime_error("Error Opening up file");
		}

		size_t fileSize = (size_t)input.tellg();
		std::vector<char> buffer(fileSize);

		OP_CORE_INFO("FileSize: {0}", fileSize);

		input.seekg(0);
		input.read(buffer.data(), fileSize);
		input.close();

		return buffer;
	}

